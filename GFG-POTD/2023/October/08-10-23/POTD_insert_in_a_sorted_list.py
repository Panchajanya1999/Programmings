class Solution:
    def sortedInsert(self, head1,key):
        # code here
        # return head of edited linked list
        
        # create a new node of the key
        newnode = Node(key)
        
        # check if the list is empty or new data is smaller than the head
        if head1 is None or head1.data > key:
            newnode.next = head1
            head1 = newnode
            return head1
        
        # traverse the whole list
        curr = head1
        while curr.next is not None and curr.next.data < key:
            curr = curr.next
        
        # insert the new node
        newnode.next = curr.next
        curr.next = newnode
        
        return head1


